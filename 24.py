import re
from collections import defaultdict
l = s.splitlines()

directions = re.compile(r"e|w|se|sw|ne|nw")

def traverse(line):
    x,y,z = 0,0,0

    for move in directions.findall(line):
        if move == "e":
            x += 1
            y -= 1
        elif move == "w":
            x -= 1
            y += 1
        elif move == "ne":
            x += 1
            z -= 1
        elif move == "nw":
            y += 1
            z -= 1
        elif move == "se":
            y -= 1
            z += 1
        elif move == "sw":
            x -= 1
            z += 1
    
    return x,y,z

def traverse_all(lines):
    tiles = defaultdict(int)

    for line in lines:
        result = traverse(line)
        tiles[result] = (tiles[result] + 1) % 2
    
    return tiles

